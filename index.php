<script>
  document.addEventListener("DOMContentLoaded", function() {
    const cookieBanner = document.getElementById("cookieConsent");
    const acceptBtn = document.getElementById("acceptCookies");
    const declineBtn = document.getElementById("declineCookies");

    // Function to check if banner should appear (no consent or expired)
    function shouldShowBanner() {
      const consentData = JSON.parse(localStorage.getItem("cookieConsentData"));
      if (!consentData) return true;

      const { timestamp } = consentData;
      const oneDay = 24 * 60 * 60 * 1000; // 24 hours
      const now = new Date().getTime();
      return (now - timestamp) > oneDay;
    }

    // Show banner with slide-in animation
    if (shouldShowBanner()) {
      setTimeout(() => cookieBanner.classList.add("show"), 500);
    }

    // Save user choice in localStorage with timestamp
    function saveConsent(choice) {
      const consentData = {
        choice: choice,
        timestamp: new Date().getTime()
      };
      localStorage.setItem("cookieConsentData", JSON.stringify(consentData));
      cookieBanner.classList.remove("show");
    }

    acceptBtn.addEventListener("click", () => saveConsent("accepted"));
    declineBtn.addEventListener("click", () => saveConsent("declined"));
  });
</script>


